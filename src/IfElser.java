public class IfElser {
    public static void main(String[] args) {
        int number = 15;

        if (number > 20) {
            System.out.println("Number is greater than 20");
        } else if (number > 10) {
            System.out.println("Number is greater than 10 but not greater than 20");
        } else {
            System.out.println("Number is 10 or less");
        }
    }
}
