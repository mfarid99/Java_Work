public class Datacasting {
    public static void main(String[] args) {
        int num = 5;
        System.out.println(num);

        float num2 = 7;
        System.out.println(num2); // 7.0

        int num3 = (int) 3.14;
        System.out.println(num3); //3

        float num4 = 99;
        System.out.println(num4); //99.00

        int num5 = 7;
        float num6 = (float) num5;
        double num7 = (double) num5;
        System.out.println(num6); //7.0

        float num8 = 4.94f;
        int num9 = (int) num8;
        System.out.println(num9); //4



    }
}
